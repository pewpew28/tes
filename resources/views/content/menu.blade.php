            const char = text[i];

            // Create a span element for the character
            const charSpan = document.createElement('span');
            charSpan.textContent = char;
            charSpan.classList.add('char');

            // Calculate the rotation angle for the character
            const rotation = angle * i;

            // Set the rotation and position for the character
            charSpan.style.transform = `rotate(${rotation}deg) translate(2.5em) rotate(-${rotation}deg)`;

            // Append the character span to the text container
